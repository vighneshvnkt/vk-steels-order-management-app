    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String s = this.name.getText();
        String addr = this.address.getText();
        String vat = this.vat.getText();
        String cst = this.cst.getText();
        String dead = this.deadline.getText();
        try {

            if (!DBHandler.getResult("Select * from clients where c_name='" + s + "'").next()) {
                DBHandler.con.close();
                String query = "Insert into clients values('" + s + "','" + addr + "','" + vat + "','" + cst + "')";
                DBHandler.insert(query);
            }
            DBHandler.con.close();
        } catch (Exception ex) {
        }
        float total_tax = 0, total = 0;
        try {
            ResultSet set = DBHandler.getResult("Select seq from sqlite_sequence where name='sales'");
            int no = 0;
            if (set.next()) {
                no = set.getInt("seq");
            }
            no++;
            DBHandler.con.close();
            String query = "insert into sales (tax_invoice_no,client,deadline) "
                    + "Values('" + "VK10000" + no + "','" + s + "','" + dead + "');";
            DBHandler.insert(query);
            DBHandler.con.close();
            for (int i = 0; i < array.size(); i++) {
                OrderDetails o = array.get(i);
                String query2 = "Insert into orders(description,rate,quantity,kind,sno,tax,total)"
                        + " values('" + o.description + "','" + o.rate + "','" + o.quantity
                        + "','" + o.no + "','" + no + "','" + (o.rate * o.quantity * 0.05f) + "','"
                        + (o.rate * o.quantity * 1.05f) + "')";
                total_tax += (o.rate * o.quantity * 0.05f);
                total += (o.rate * o.quantity * 1.05f);
                DBHandler.insert(query2);
            }
            String q = "Update sales set tax_computation='"+total_tax+"', total='"+total+"' where sno='"+no+"'";
            DBHandler.addData(q);
            JOptionPane.showMessageDialog(this, "Orders added");
            new Main().setVisible(true);
            this.dispose();
        } catch (Exception ex) {

        }


    }//GEN-LAST:event_jButton7ActionPerformed

