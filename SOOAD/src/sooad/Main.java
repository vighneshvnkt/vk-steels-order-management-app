    private void nameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                ResultSet res = DBHandler.getResult("SELECT * from clients where c_name='" + this.name.getText() + "'");
                if (res.next()) {
                    this.address.setText(res.getString("c_address"));
                    this.vat.setText(res.getString("vat_tin"));
                    this.cst.setText(res.getString("cst_no"));
                }
                DBHandler.con.close();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_nameKeyPressed

